/**
 * Normalize a placeholder key extracted from {{...}}:
 * 1. Trim surrounding whitespace
 * 2. Collapse spaces around "/" → "A / B" becomes "A/B"
 * 3. Remove spaces between ASCII digit and Chinese character → "3 类" becomes "3类"
 */
function normalizeKey(raw: string): string {
  return raw
    .trim()
    .replace(/\s*\/\s*/g, "/")
    .replace(/(\d+)\s+(?=[\u4e00-\u9fa5])/g, "$1")
}

function renderTemplate(template: string, variables: Record<string, string>) {
  return template.replace(/\{\{([^{}]+)\}\}/g, (_, key: string) => {
    const normalized = normalizeKey(key)
    const alias = VARIABLE_ALIASES[normalized]
    return variables[normalized] ?? (alias ? variables[alias] : undefined) ?? `{{${normalized}}}`
  })
}

function renderQuestions(questions: readonly QuestionTemplate[], variables: Record<string, string>): RenderedQuestion[] {
  return questions.map((question) => ({
    ...question,
    renderedStem: renderTemplate(question.stem, variables),
    renderedOptionA: renderTemplate(question.optionA, variables),
    renderedOptionB: renderTemplate(question.optionB, variables),
    renderedSupport: question.support ? renderTemplate(question.support, variables) : undefined,
    renderedLogic: renderTemplate(question.logic, variables),
  }))
}

export function resolveTrack(rawTrack?: string | null) {
  const normalized = rawTrack?.trim().toLowerCase()
  const directTrackId =
    normalized && normalized in TRACK_PACKAGES
      ? (normalized as keyof typeof TRACK_PACKAGES)
      : undefined
  const trackId = directTrackId ?? (normalized ? TRACK_ALIASES[normalized as keyof typeof TRACK_ALIASES] : undefined) ?? TRACK_ALIASES.default
  return TRACK_PACKAGES[trackId]
}

export function buildAssessmentForTrack(rawTrack?: string | null) {
  const track = resolveTrack(rawTrack)
  return {
    track,
    academicQuestions: renderQuestions(ACADEMIC_TEMPLATES, track.variables),
    careerQuestions: renderQuestions(CAREER_TEMPLATES, track.variables),
  }
}

export function choiceToLikert(choice?: BinaryChoice, highOption: BinaryChoice = "B") {
  if (!choice) return 3
  return choice === highOption ? 5 : 1
}
